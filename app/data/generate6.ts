type Question = {
  genre: string;
  question: string;
  answer: string;
  explanation: string;
};

type RonriTemplate = [string, string, string];
type PatternTemplate = [string, string, string];

export function generate6Questions() {

  const sansu: Question[] = [];
  const ronri: Question[] = [];
  const pattern: Question[] = [];
  const hiragana: Question[] = [];
  const nakamawake: Question[] = [];

  // ====================
  // さんすう（たしざん・ひきざん・ぶんしょうだい）
  // ====================

  const addTemplates = [
    (a: number, b: number) => `${a} + ${b} = ?`,
    (a: number, b: number) => `おりがみが ${a} まいと ${b} まい。あわせてなんまい？`,
    (a: number, b: number) => `あめが ${a} こと ${b} こ。ぜんぶでいくつ？`,
    (a: number, b: number) => `シールが ${a} まいと ${b} まい。あわせてなんまい？`,
  ];

  const subTemplates = [
    (a: number, b: number) => `${a} - ${b} = ?`,
    (a: number, b: number) => `クッキーが ${a} こありました。${b} こたべました。のこりはいくつ？`,
    (a: number, b: number) => `えんぴつが ${a} ほんあります。${b} ほんつかいました。のこりはなんぼん？`,
    (a: number, b: number) => `おかしが ${a} こあります。ともだちに ${b} こあげました。のこりはいくつ？`,
  ];

  let addIndex = 0;
  let subIndex = 0;

  for (let a = 1; a <= 30; a++) {
    for (let b = 1; b <= 30; b++) {

      const addTpl = addTemplates[addIndex % addTemplates.length];
      sansu.push({
        genre: "🔢 さんすう",
        question: addTpl(a, b),
        answer: `${a + b}`,
        explanation: `${a} と ${b} を たすと ${a + b} です。`,
      });
      addIndex++;

      if (a > b) {
        const subTpl = subTemplates[subIndex % subTemplates.length];
        sansu.push({
          genre: "🔢 さんすう",
          question: subTpl(a, b),
          answer: `${a - b}`,
          explanation: `${a} から ${b} を ひくと ${a - b} です。`,
        });
        subIndex++;
      }
    }
  }

  // ====================
  // ろんり
  // ====================

  const ronriTemplates: RonriTemplate[] = [
    ["AはBよりおおきい。BはCよりおおきい。いちばん小さいのは？", "C", "A>B>Cの じゅんなので、いちばん小さいのはCです。"],
    ["りんご・ばなな・みかん・くるま → ちがうのは？", "くるま", "りんご・ばなな・みかんは くだものですが、くるまだけ ちがう なかまです。"],
    ["あさ→ひる→よる→あさ→□", "ひる", "あさ・ひる・よるの じゅんばんが くりかえします。"],
    ["1→3→5→7→□", "9", "2ずつ ふえています。"],
    ["あか→あお→あか→あお→□", "あか", "あかと あおが こうごに ならんでいます。"],
    ["げつよう→かよう→すいよう→もくよう→□", "きんよう", "ようびの じゅんに すすんでいます。"],
    ["はる→なつ→あき→ふゆ→□", "はる", "きせつの じゅんばんが くりかえします。"],
    ["3→6→9→12→□", "15", "3ずつ ふえています。"],
    ["10→9→8→7→□", "6", "1ずつ へっています。"],
    ["AはBよりはやい。BはCよりはやい。いちばんおそいのは？", "C", "A>B>Cの じゅんなので、いちばんおそいのはCです。"],
    ["いぬ・ねこ・ぞう・でんしゃ → ちがうのは？", "でんしゃ", "いぬ・ねこ・ぞうは どうぶつですが、でんしゃだけ ちがう なかまです。"],
    ["50→40→30→□", "20", "10ずつ へっています。"],
    ["まる→さんかく→しかく→まる→□", "さんかく", "まる・さんかく・しかくの じゅんに くりかえします。"],
    ["あつい→さむい→あつい→さむい→□", "あつい", "あついと さむいが こうごに ならんでいます。"],
    ["1にち→7にち→14にち→□", "21にち", "7ずつ ふえています。"],
    ["4→8→16→□", "32", "まえの かずを 2ばいすると つぎの かずに なります。"],
    ["げつよう→すいよう→きんよう→□", "にちよう", "1にちおきに ようびが すすんでいます。"],
    ["100→80→60→□", "40", "20ずつ へっています。"],
    ["AはBよりおもい。BはCよりおもい。いちばんかるいのは？", "C", "A>B>Cの じゅんなので、いちばんかるいのはCです。"],
    ["1がつ→3がつ→5がつ→□", "7がつ", "2かげつずつ すすんでいます。"],
  ];

  for (let i = 0; i < 14; i++) {
    ronriTemplates.forEach(t => {
      ronri.push({
        genre: "🧠 ろんり",
        question: t[0],
        answer: t[1],
        explanation: t[2],
      });
    });
  }

  // ====================
  // パターン
  // ====================

  const patternTemplates: PatternTemplate[] = [
    ["2 4 6 8 □", "10", "2ずつ ふえています。"],
    ["1 3 5 7 □", "9", "2ずつ ふえています。"],
    ["A B A B A □", "B", "AとBが こうごに ならんでいます。"],
    ["○ △ ○ △ ○ □", "△", "○と△が こうごに ならんでいます。"],
    ["10 20 30 40 □", "50", "10ずつ ふえています。"],
    ["5 10 15 20 □", "25", "5ずつ ふえています。"],
    ["100 90 80 □", "70", "10ずつ へっています。"],
    ["1 2 4 8 □", "16", "まえの かずを 2ばいすると つぎの かずに なります。"],
    ["あ い う あ い □", "う", "あ・い・うの じゅんに くりかえします。"],
    ["● ▲ ■ ● ▲ □", "■", "●・▲・■の じゅんに くりかえします。"],
    ["3 6 9 12 □", "15", "3ずつ ふえています。"],
    ["20 15 10 □", "5", "5ずつ へっています。"],
    ["赤 青 黄 赤 青 □", "黄", "赤・青・黄の じゅんに くりかえします。"],
    ["1 1 2 3 5 □", "8", "まえの2つの かずを たすと つぎの かずに なります。"],
    ["7 14 21 28 □", "35", "7ずつ ふえています。"],
    ["A B B A B □", "B", "A・B・Bの じゅんに くりかえします。"],
    ["2 3 5 8 □", "13", "まえの2つの かずを たすと つぎの かずに なります。"],
    ["90 80 70 60 □", "50", "10ずつ へっています。"],
    ["◆ ◇ ◆ ◇ ◆ □", "◇", "◆と◇が こうごに ならんでいます。"],
    ["6 12 18 24 □", "30", "6ずつ ふえています。"],
  ];

  for (let i = 0; i < 14; i++) {
    patternTemplates.forEach(t => {
      pattern.push({
        genre: "🔷 パターン",
        question: `${t[0]} → □にはいるのは？`,
        answer: t[1],
        explanation: t[2],
      });
    });
  }

  // ====================
  // ひらがな（よみ・いみ）
  // ====================

  const words: { w: string; a: string }[] = [
    { w: "りんご", a: "くだもの" },
    { w: "みかん", a: "くだもの" },
    { w: "ばなな", a: "くだもの" },
    { w: "すいか", a: "くだもの" },
    { w: "いぬ", a: "どうぶつ" },
    { w: "ねこ", a: "どうぶつ" },
    { w: "ぞう", a: "どうぶつ" },
    { w: "きりん", a: "どうぶつ" },
    { w: "でんしゃ", a: "のりもの" },
    { w: "ばす", a: "のりもの" },
    { w: "くるま", a: "のりもの" },
    { w: "ひこうき", a: "のりもの" },
    { w: "えんぴつ", a: "がっこうのもの" },
    { w: "ノート", a: "がっこうのもの" },
    { w: "はさみ", a: "がっこうのもの" },
    { w: "けしごむ", a: "がっこうのもの" },
    { w: "ぼうし", a: "きるもの" },
    { w: "くつ", a: "きるもの" },
    { w: "はぶらし", a: "せいけつのもの" },
    { w: "たいよう", a: "そらのもの" },
    { w: "つき", a: "そらのもの" },
    { w: "ほし", a: "そらのもの" },
  ];

  for (let i = 0; i < 12; i++) {
    words.forEach(w => {
      hiragana.push({
        genre: "🔤 ことば",
        question: `「${w.w}」はどんななかま？`,
        answer: w.a,
        explanation: `「${w.w}」は${w.a}の なかまです。`,
      });
    });
  }

  // ====================
  // なかまわけ
  // ====================

  const groups: { q: string; a: string }[] = [
    { q: "どうぶつをえらぼう：いぬ・ねこ・ぞう・でんしゃ", a: "いぬ ねこ ぞう" },
    { q: "のりものをえらぼう：ばす・でんしゃ・ひこうき・りんご", a: "ばす でんしゃ ひこうき" },
    { q: "たべものをえらぼう：ぱん・ごはん・おにぎり・くるま", a: "ぱん ごはん おにぎり" },
    { q: "がっこうのものをえらぼう：えんぴつ・ノート・けしごむ・ぞう", a: "えんぴつ ノート けしごむ" },
    { q: "いろをえらぼう：あか・あお・きいろ・ばす", a: "あか あお きいろ" },
    { q: "やさいをえらぼう：にんじん・とまと・きゅうり・ねこ", a: "にんじん とまと きゅうり" },
    { q: "きせつをえらぼう：はる・なつ・あき・えんぴつ", a: "はる なつ あき" },
    { q: "そらのものをえらぼう：たいよう・つき・ほし・つくえ", a: "たいよう つき ほし" },
    { q: "むしをえらぼう：ちょうちょ・ありんこ・せみ・ばす", a: "ちょうちょ ありんこ せみ" },
    { q: "きるものをえらぼう：ぼうし・くつした・てぶくろ・つくえ", a: "ぼうし くつした てぶくろ" },
    { q: "かぐをえらぼう：いす・つくえ・べっど・ねこ", a: "いす つくえ べっど" },
    { q: "からだをえらぼう：て・あし・め・つくえ", a: "て あし め" },
    { q: "がっきをえらぼう：たいこ・ぴあの・らっぱ・いぬ", a: "たいこ ぴあの らっぱ" },
    { q: "スポーツをえらぼう：サッカー・やきゅう・すいえい・りんご", a: "サッカー やきゅう すいえい" },
    { q: "みずのいきものをえらぼう：さかな・たこ・かに・とり", a: "さかな たこ かに" },
    { q: "とりものをえらぼう：すずめ・からす・はと・ねこ", a: "すずめ からす はと" },
    { q: "ちょうみりょうをえらぼう：しお・さとう・しょうゆ・くるま", a: "しお さとう しょうゆ" },
    { q: "きこうをえらぼう：あめ・ゆき・はれ・いぬ", a: "あめ ゆき はれ" },
    { q: "がっこうのじかんわりをえらぼう：こくご・さんすう・たいいく・ぞう", a: "こくご さんすう たいいく" },
    { q: "はなをえらぼう：さくら・ちゅーりっぷ・ひまわり・ばす", a: "さくら ちゅーりっぷ ひまわり" },
  ];

  for (let i = 0; i < 12; i++) {
    groups.forEach(g => {
      nakamawake.push({
        genre: "📦 なかまわけ",
        question: g.q,
        answer: g.a,
        explanation: `なかまは 「${g.a}」です。`,
      });
    });
  }

  return {
    sansu,
    ronri,
    pattern,
    hiragana,
    nakamawake,
  };
}
