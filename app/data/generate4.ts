type Question = {
  genre: string;
  question: string;
  answer: string;
  explanation: string;
};

export function generate4Questions() {

  const sansu: Question[] = [];
  const ronri: Question[] = [];
  const pattern: Question[] = [];
  const hiragana: Question[] = [];
  const nakamawake: Question[] = [];

  // ====================
  // さんすう（たしざん・ぶんしょうだい）
  // ====================
  const sansuTemplates = [
    (a: number, b: number) => `おかしが ${a} こあって ${b} こふえたら？`,
    (a: number, b: number) => `りんごが ${a} こと ${b} こ。あわせていくつ？`,
    (a: number, b: number) => `ちょうちょが ${a} ひきと ${b} ひき。ぜんぶでなんびき？`,
    (a: number, b: number) => `ふうせんが ${a} こと ${b} こ。あわせていくつ？`,
    (a: number, b: number) => `ほんが ${a} さつと ${b} さつ。あわせてなんさつ？`,
    (a: number, b: number) => `くるまが ${a} だいと ${b} だい。ぜんぶでなんだい？`,
  ];

  let sansuIndex = 0;
  for (let a = 1; a <= 10; a++) {
    for (let b = 1; b <= 10; b++) {
      const tpl = sansuTemplates[sansuIndex % sansuTemplates.length];
      sansu.push({
        genre: "さんすう",
        question: tpl(a, b),
        answer: `${a + b}`,
        explanation: `${a} と ${b} を あわせると ${a + b} だよ。`,
      });
      sansuIndex++;
    }
  }

  // ====================
  // しこうパズル（じゅんばん・きまり）
  // ====================
  const ronriBase: [string, string, string][] = [
    ["【ならび】1 → 2 → 3 → □", "4", "1ずつ ふえているよ。"],
    ["【きまり】あか → あお → あか → □", "あお", "あかと あおが こうごに ならんでいるよ。"],
    ["【ならび】2 → 4 → 6 → □", "8", "2ずつ ふえているよ。"],
    ["【きまり】おおきい → ちいさい → おおきい → □", "ちいさい", "おおきいと ちいさいが こうごに ならんでいるよ。"],
    ["【じゅんばん】あさ → ひる → よる → □", "あさ", "あさ・ひる・よるの じゅんばんが くりかえすよ。"],
    ["【きまり】まる → さんかく → まる → □", "さんかく", "まると さんかくが こうごに ならんでいるよ。"],
    ["【ならび】1 → 2 → 1 → 2 → □", "1", "1と 2が こうごに ならんでいるよ。"],
    ["【きまり】たかい → ひくい → たかい → □", "ひくい", "たかいと ひくいが こうごに ならんでいるよ。"],
    ["【じゅんばん】はる → なつ → あき → □", "ふゆ", "きせつの じゅんばんが くりかえすよ。"],
    ["【きまり】あお → きいろ → あお → □", "きいろ", "あおと きいろが こうごに ならんでいるよ。"],
    ["【きまり】うえ → した → うえ → □", "した", "うえと したが こうごに ならんでいるよ。"],
    ["【ならび】3 → 6 → 9 → □", "12", "3ずつ ふえているよ。"],
    ["【きまり】おおきい こえ → ちいさい こえ → おおきい こえ → □", "ちいさい こえ", "こえの おおきさが こうごに かわるよ。"],
    ["【じゅんばん】1にち → 2にち → 3にち → □", "4にち", "1にちずつ すすんでいるよ。"],
    ["【ならび】4 → 5 → 6 → □", "7", "1ずつ ふえているよ。"],
    ["【きまり】ながい → みじかい → ながい → □", "みじかい", "ながいと みじかいが こうごに ならんでいるよ。"],
    ["【じゅんばん】1がつ → 2がつ → 3がつ → □", "4がつ", "1かげつずつ すすんでいるよ。"],
    ["【きまり】おもい → かるい → おもい → □", "かるい", "おもいと かるいが こうごに ならんでいるよ。"],
    ["【ならび】5 → 10 → 15 → □", "20", "5ずつ ふえているよ。"],
    ["【きまり】あける → しめる → あける → □", "しめる", "あけると しめるが こうごに ならんでいるよ。"],
  ];

  for (let i = 0; i < 20; i++) {
    ronriBase.forEach(t => {
      ronri.push({
        genre: "ろんり",
        question: `${t[0]} つぎはどれかな？`,
        answer: t[1],
        explanation: t[2],
      });
    });
  }

  // ====================
  // パターン
  // ====================
  const patternBase: [string, string, string][] = [
    ["○ △ ○ △ □", "○", "○と △が こうごに ならんでいるよ。"],
    ["1 2 1 2 □", "1", "1と 2が こうごに ならんでいるよ。"],
    ["あ い あ い □", "あ", "「あ」と「い」が こうごに ならんでいるよ。"],
    ["★ ♥ ★ ♥ □", "★", "★と ♥が こうごに ならんでいるよ。"],
    ["あか あお あか あお □", "あか", "あかと あおが こうごに ならんでいるよ。"],
    ["おお ちい おお ちい □", "おお", "おおきいと ちいさいが こうごに ならんでいるよ。"],
    ["いぬ ねこ いぬ ねこ □", "いぬ", "いぬと ねこが こうごに ならんでいるよ。"],
    ["□ さんかく まる さんかく まる", "さんかく", "さんかくと まるが こうごに ならんでいるよ。"],
    ["1 3 1 3 □", "1", "1と 3が こうごに ならんでいるよ。"],
    ["き みどり き みどり □", "き", "きいろと みどりが こうごに ならんでいるよ。"],
    ["うえ した うえ した □", "うえ", "うえと したが こうごに ならんでいるよ。"],
    ["● ▲ ● ▲ □", "●", "●と ▲が こうごに ならんでいるよ。"],
    ["2 4 2 4 □", "2", "2と 4が こうごに ならんでいるよ。"],
    ["すき きらい すき きらい □", "すき", "「すき」と「きらい」が こうごに ならんでいるよ。"],
    ["おおい すくない おおい すくない □", "おおい", "「おおい」と「すくない」が こうごに ならんでいるよ。"],
    ["さかな とり さかな とり □", "さかな", "さかなと とりが こうごに ならんでいるよ。"],
    ["は な は な □", "は", "「は」と「な」が こうごに ならんでいるよ。"],
    ["3 5 3 5 □", "3", "3と 5が こうごに ならんでいるよ。"],
    ["みぎ ひだり みぎ ひだり □", "みぎ", "みぎと ひだりが こうごに ならんでいるよ。"],
    ["6 7 6 7 □", "6", "6と 7が こうごに ならんでいるよ。"],
  ];

  for (let i = 0; i < 20; i++) {
    patternBase.forEach(t => {
      pattern.push({
        genre: "パターン",
        question: `${t[0]} つぎはどれかな？`,
        answer: t[1],
        explanation: t[2],
      });
    });
  }

  // ====================
  // ひらがな（なかま）
  // ====================
  const words = [
    { w: "りんご", a: "くだもの" },
    { w: "ばなな", a: "くだもの" },
    { w: "みかん", a: "くだもの" },
    { w: "いぬ", a: "どうぶつ" },
    { w: "ねこ", a: "どうぶつ" },
    { w: "ぞう", a: "どうぶつ" },
    { w: "でんしゃ", a: "のりもの" },
    { w: "ばす", a: "のりもの" },
    { w: "くるま", a: "のりもの" },
    { w: "えんぴつ", a: "がっこう" },
    { w: "ノート", a: "がっこう" },
    { w: "ぼうし", a: "きるもの" },
    { w: "くつ", a: "はくもの" },
    { w: "あめ", a: "てんき" },
    { w: "ゆき", a: "てんき" },
    { w: "いす", a: "かぐ" },
    { w: "つくえ", a: "かぐ" },
    { w: "ぶどう", a: "くだもの" },
    { w: "きりん", a: "どうぶつ" },
    { w: "じてんしゃ", a: "のりもの" },
    { w: "けしごむ", a: "がっこう" },
  ];

  for (let i = 0; i < 15; i++) {
    words.forEach(w => {
      hiragana.push({
        genre: "ことば",
        question: `【なかま】「${w.w}」はどれかな？`,
        answer: w.a,
        explanation: `「${w.w}」は${w.a}の なかまだよ。`,
      });
    });
  }

  // ====================
  // なかまわけ
  // ====================
  const groups: { c: string; items: string[]; a: string }[] = [
    { c: "どうぶつ", items: ["いぬ", "ねこ", "ぞう", "でんしゃ"], a: "いぬ ねこ ぞう" },
    { c: "のりもの", items: ["ばす", "でんしゃ", "くるま", "りんご"], a: "ばす でんしゃ くるま" },
    { c: "くだもの", items: ["りんご", "ばなな", "みかん", "いす"], a: "りんご ばなな みかん" },
    { c: "がっこうのもの", items: ["えんぴつ", "ノート", "けしごむ", "ぞう"], a: "えんぴつ ノート けしごむ" },
    { c: "いろ", items: ["あか", "あお", "きいろ", "ばす"], a: "あか あお きいろ" },
    { c: "きるもの", items: ["ぼうし", "くつした", "てぶくろ", "つくえ"], a: "ぼうし くつした てぶくろ" },
    { c: "てんき", items: ["あめ", "ゆき", "はれ", "いぬ"], a: "あめ ゆき はれ" },
    { c: "かぐ", items: ["いす", "つくえ", "べっど", "ねこ"], a: "いす つくえ べっど" },
    { c: "むし", items: ["ちょうちょ", "ありんこ", "せみ", "でんしゃ"], a: "ちょうちょ ありんこ せみ" },
    { c: "やさい", items: ["にんじん", "とまと", "きゅうり", "いぬ"], a: "にんじん とまと きゅうり" },
    { c: "きせつ", items: ["はる", "なつ", "あき", "ばす"], a: "はる なつ あき" },
    { c: "そらのもの", items: ["たいよう", "つき", "ほし", "つくえ"], a: "たいよう つき ほし" },
    { c: "からだ", items: ["て", "あし", "め", "いす"], a: "て あし め" },
    { c: "おと", items: ["わんわん", "にゃーにゃー", "もーもー", "ばす"], a: "わんわん にゃーにゃー もーもー" },
    { c: "がっき", items: ["たいこ", "ぴあの", "らっぱ", "いぬ"], a: "たいこ ぴあの らっぱ" },
    { c: "おうちのもの", items: ["まど", "どあ", "かぎ", "ぞう"], a: "まど どあ かぎ" },
    { c: "みずのいきもの", items: ["さかな", "たこ", "かに", "とり"], a: "さかな たこ かに" },
    { c: "とりもの", items: ["すずめ", "からす", "はと", "ねこ"], a: "すずめ からす はと" },
    { c: "あじ", items: ["あまい", "しょっぱい", "すっぱい", "つくえ"], a: "あまい しょっぱい すっぱい" },
    { c: "はな", items: ["さくら", "ちゅーりっぷ", "ひまわり", "ばす"], a: "さくら ちゅーりっぷ ひまわり" },
  ];

  for (let i = 0; i < 15; i++) {
    groups.forEach(g => {
      nakamawake.push({
        genre: "なかまわけ",
        question: `【${g.c}】${g.items.join("・")} どれがなかまかな？`,
        answer: g.a,
        explanation: `${g.c}の なかまは 「${g.a}」だよ。`,
      });
    });
  }

  return { sansu, ronri, pattern, hiragana, nakamawake };
}
