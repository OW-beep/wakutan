type Question = {
  genre: string;
  question: string;
  answer: string;
  explanation: string;
};

export function generate5Questions() {

  const sansu: Question[] = [];
  const ronri: Question[] = [];
  const pattern: Question[] = [];
  const hiragana: Question[] = [];
  const nakamawake: Question[] = [];

  // ====================
  // さんすう（たしざん・ぶんしょうだい）
  // ====================
  const sansuTemplates = [
    (a: number, b: number) => `${a} + ${b} = ?`,
    (a: number, b: number) => `あめが ${a} こと ${b} こ。あわせていくつ？`,
    (a: number, b: number) => `どんぐりが ${a} こと ${b} こ。ぜんぶでいくつ？`,
    (a: number, b: number) => `おりがみが ${a} まいと ${b} まい。あわせてなんまい？`,
    (a: number, b: number) => `シールが ${a} まいと ${b} まい。ぜんぶでなんまい？`,
    (a: number, b: number) => `クッキーが ${a} こと ${b} こ。あわせていくつ？`,
  ];

  let sansuIndex = 0;
  for (let a = 1; a <= 20; a++) {
    for (let b = 1; b <= 20; b++) {
      const tpl = sansuTemplates[sansuIndex % sansuTemplates.length];
      sansu.push({
        genre: "さんすう",
        question: tpl(a, b),
        answer: `${a + b}`,
        explanation: `${a} と ${b} を あわせると ${a + b} だよ。`,
      });
      sansuIndex++;
    }
  }

  // ====================
  // しこうパズル（ルール理解）
  // ====================
  const ronliBase: [string, string, string][] = [
    ["【ならび】1 → 3 → 5 → 7 → □", "9", "2ずつ ふえているよ。"],
    ["【きまり】あか → あお → あか → あお → □", "あか", "あかと あおが こうごに ならんでいるよ。"],
    ["【じゅんばん】あさ → ひる → よる → □", "あさ", "あさ・ひる・よるの じゅんばんが くりかえすよ。"],
    ["【ならび】2 → 4 → 6 → 8 → □", "10", "2ずつ ふえているよ。"],
    ["【きまり】おおきい → ふつう → ちいさい → おおきい → □", "ふつう", "おおきい・ふつう・ちいさいの じゅんに くりかえすよ。"],
    ["【じゅんばん】はる → なつ → あき → ふゆ → □", "はる", "きせつの じゅんばんが くりかえすよ。"],
    ["【ならび】10 → 8 → 6 → □", "4", "2ずつ へっているよ。"],
    ["【きまり】まる → さんかく → しかく → まる → □", "さんかく", "まる・さんかく・しかくの じゅんに くりかえすよ。"],
    ["【ならび】5 → 10 → 15 → □", "20", "5ずつ ふえているよ。"],
    ["【きまり】あさごはん → ひるごはん → ばんごはん → □", "あさごはん", "ごはんの じゅんばんが くりかえすよ。"],
    ["AはBよりせが たかい。BはCよりせが たかい。いちばんひくいのはだれ？", "C", "A>B>Cの じゅんなので、いちばん ひくいのはCだよ。"],
    ["げつよう → かよう → すいよう → □", "もくよう", "ようびの じゅんに すすんでいるよ。"],
    ["【ならび】20 → 15 → 10 → □", "5", "5ずつ へっているよ。"],
    ["【きまり】あつい → さむい → あつい → さむい → □", "あつい", "あついと さむいが こうごに ならんでいるよ。"],
    ["【ならび】4 → 8 → 12 → □", "16", "4ずつ ふえているよ。"],
    ["【きまり】あける → しめる → あける → しめる → □", "あける", "あけると しめるが こうごに ならんでいるよ。"],
    ["【じゅんばん】1がつ → 2がつ → 3がつ → □", "4がつ", "1かげつずつ すすんでいるよ。"],
    ["【ならび】30 → 25 → 20 → □", "15", "5ずつ へっているよ。"],
    ["【きまり】ながい → みじかい → ふつう → ながい → □", "みじかい", "ながい・みじかい・ふつうの じゅんに くりかえすよ。"],
    ["AはBよりおもい。BはCよりおもい。いちばんかるいのは？", "C", "A>B>Cの じゅんなので、いちばん かるいのはCだよ。"],
  ];

  for (let i = 0; i < 18; i++) {
    ronliBase.forEach(t => {
      ronri.push({
        genre: "ろんり",
        question: `${t[0]} つぎはどれかな？`,
        answer: t[1],
        explanation: t[2],
      });
    });
  }

  // ====================
  // パターン
  // ====================
  const patternBase: [string, string, string][] = [
    ["1 2 1 2 1 □", "2", "1と 2が こうごに ならんでいるよ。"],
    ["あ い あ い □", "あ", "「あ」と「い」が こうごに ならんでいるよ。"],
    ["○ △ ○ △ □", "○", "○と △が こうごに ならんでいるよ。"],
    ["2 4 2 4 2 □", "4", "2と 4が こうごに ならんでいるよ。"],
    ["あか あお きいろ あか あお □", "きいろ", "あか・あお・きいろの じゅんに くりかえすよ。"],
    ["1 2 3 1 2 3 □", "1", "1・2・3の じゅんに くりかえすよ。"],
    ["★ ★ ♥ ★ ★ □", "♥", "★が2つ、♥が1つの じゅんに くりかえすよ。"],
    ["おお ちゅう ちい おお ちゅう □", "ちい", "おお・ちゅう・ちいの じゅんに くりかえすよ。"],
    ["いぬ ねこ とり いぬ ねこ □", "とり", "いぬ・ねこ・とりの じゅんに くりかえすよ。"],
    ["5 10 15 5 10 □", "15", "5・10・15の じゅんに くりかえすよ。"],
    ["● ▲ ■ ● ▲ □", "■", "●・▲・■の じゅんに くりかえすよ。"],
    ["うえ みぎ した ひだり うえ □", "みぎ", "うえ・みぎ・した・ひだりの じゅんに くりかえすよ。"],
    ["3 6 3 6 3 □", "6", "3と 6が こうごに ならんでいるよ。"],
    ["さかな とり むし さかな とり □", "むし", "さかな・とり・むしの じゅんに くりかえすよ。"],
    ["おおい ふつう すくない おおい ふつう □", "すくない", "おおい・ふつう・すくないの じゅんに くりかえすよ。"],
    ["A B C A B □", "C", "A・B・Cの じゅんに くりかえすよ。"],
    ["4 8 4 8 4 □", "8", "4と 8が こうごに ならんでいるよ。"],
    ["すき きらい ふつう すき きらい □", "ふつう", "すき・きらい・ふつうの じゅんに くりかえすよ。"],
    ["10 9 8 10 9 □", "8", "10・9・8の じゅんに くりかえすよ。"],
    ["7 8 7 8 □", "7", "7と 8が こうごに ならんでいるよ。"],
  ];

  for (let i = 0; i < 18; i++) {
    patternBase.forEach(t => {
      pattern.push({
        genre: "パターン",
        question: `${t[0]} → つぎはどれかな？`,
        answer: t[1],
        explanation: t[2],
      });
    });
  }

  // ====================
  // ひらがな（なかま理解）
  // ====================
  const words = [
    { w: "りんご", a: "くだもの" },
    { w: "ぶどう", a: "くだもの" },
    { w: "いちご", a: "くだもの" },
    { w: "いぬ", a: "どうぶつ" },
    { w: "きりん", a: "どうぶつ" },
    { w: "うさぎ", a: "どうぶつ" },
    { w: "でんしゃ", a: "のりもの" },
    { w: "ひこうき", a: "のりもの" },
    { w: "じてんしゃ", a: "のりもの" },
    { w: "えんぴつ", a: "がっこう" },
    { w: "けしごむ", a: "がっこう" },
    { w: "くつ", a: "つかうもの" },
    { w: "かさ", a: "つかうもの" },
    { w: "たいよう", a: "そら" },
    { w: "つき", a: "そら" },
    { w: "にんじん", a: "やさい" },
    { w: "とまと", a: "やさい" },
    { w: "きゅうり", a: "やさい" },
    { w: "なす", a: "やさい" },
    { w: "ぱんだ", a: "どうぶつ" },
  ];

  for (let i = 0; i < 13; i++) {
    words.forEach(w => {
      hiragana.push({
        genre: "ことば",
        question: `【なかま】「${w.w}」はどんななかまかな？`,
        answer: w.a,
        explanation: `「${w.w}」は${w.a}の なかまだよ。`,
      });
    });
  }

  // ====================
  // なかまわけ
  // ====================
  const groups: { c: string; items: string[]; a: string }[] = [
    { c: "どうぶつ", items: ["いぬ", "ねこ", "ぞう", "でんしゃ"], a: "いぬ ねこ ぞう" },
    { c: "のりもの", items: ["ばす", "でんしゃ", "くるま", "りんご"], a: "ばす でんしゃ くるま" },
    { c: "たべもの", items: ["ぱん", "ごはん", "おにぎり", "くるま"], a: "ぱん ごはん おにぎり" },
    { c: "やさい", items: ["にんじん", "とまと", "きゅうり", "ばなな"], a: "にんじん とまと きゅうり" },
    { c: "きせつ", items: ["はる", "なつ", "あき", "えんぴつ"], a: "はる なつ あき" },
    { c: "がっこうのもの", items: ["えんぴつ", "ノート", "けしごむ", "ぞう"], a: "えんぴつ ノート けしごむ" },
    { c: "そらのもの", items: ["たいよう", "つき", "ほし", "つくえ"], a: "たいよう つき ほし" },
    { c: "むし", items: ["ちょうちょ", "ありんこ", "せみ", "ばす"], a: "ちょうちょ ありんこ せみ" },
    { c: "いろ", items: ["あか", "あお", "きいろ", "でんしゃ"], a: "あか あお きいろ" },
    { c: "きるもの", items: ["ぼうし", "くつした", "てぶくろ", "つくえ"], a: "ぼうし くつした てぶくろ" },
    { c: "からだ", items: ["て", "あし", "め", "つくえ"], a: "て あし め" },
    { c: "がっき", items: ["たいこ", "ぴあの", "らっぱ", "いぬ"], a: "たいこ ぴあの らっぱ" },
    { c: "おうちのもの", items: ["まど", "どあ", "かぎ", "ぞう"], a: "まど どあ かぎ" },
    { c: "みずのいきもの", items: ["さかな", "たこ", "かに", "とり"], a: "さかな たこ かに" },
    { c: "とりもの", items: ["すずめ", "からす", "はと", "ねこ"], a: "すずめ からす はと" },
    { c: "スポーツ", items: ["サッカー", "やきゅう", "すいえい", "りんご"], a: "サッカー やきゅう すいえい" },
    { c: "ちょうみりょう", items: ["しお", "さとう", "しょうゆ", "くるま"], a: "しお さとう しょうゆ" },
    { c: "かず", items: ["いち", "に", "さん", "あか"], a: "いち に さん" },
    { c: "きこう", items: ["あめ", "ゆき", "はれ", "いぬ"], a: "あめ ゆき はれ" },
    { c: "はな", items: ["さくら", "ちゅーりっぷ", "ひまわり", "ばす"], a: "さくら ちゅーりっぷ ひまわり" },
  ];

  for (let i = 0; i < 15; i++) {
    groups.forEach(g => {
      nakamawake.push({
        genre: "なかまわけ",
        question: `【${g.c}】${g.items.join("・")}の中でなかまはどれかな？`,
        answer: g.a,
        explanation: `${g.c}の なかまは 「${g.a}」だよ。`,
      });
    });
  }

  return { sansu, ronri, pattern, hiragana, nakamawake };
}
